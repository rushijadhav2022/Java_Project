package com.javafundamentals;

public class Unary_Operator {

	public static void main(String[] args) {
		
		int x=10;
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(x--);
		System.out.println(--x);
		
		int a=10;
		int b=10;
		System.out.println(a++ + ++a);
		System.out.println(b++ + b++);
		
		int s = 6;
		int w = 3;
		System.out.println(s+w);
		System.out.println(s-w);
		System.out.println(s/w);
		System.out.println(s%w);
	
	
	}
	
}
